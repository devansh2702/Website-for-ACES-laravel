<div class="heading">Nirma University</div>
<div class="para">Renowned industrialist and philanthropist Dr Karsanbhai K Patel, the founder of the Nirma Group of Industries, established the Nirma Education and Research Foundation (NERF) in the year 1994 with a vision to promote higher education and to provide excellent educational facilities to the youth in India. In 1995, Nirma Institute of Technology – affiliated to Gujarat University – was established by the NERF. It was followed by the establishment of Nirma Institute of Management in 1996.

In the year 2003, the Government of Gujarat approved the proposal of the NERF to found the Nirma University, Ahmedabad. Hence, the Nirma University was established under a special Act passed by the Gujarat State Legislative Assembly. The University Grants Commission (UGC) duly recognised the University under Section 2 (f) of the UGC Act.

The NERF is equally committed to school education. It strongly believes in the fact that a strong foundation is a prerequisite for education. With this philosophy, it runs two schools ‘Nirma Vidyavihar’ at Bodakdev, Ahmedabad and at Chharodi, Ahmedabad. Here, it lays emphasis on providing value-based education along with innovative teaching practices.</div>
